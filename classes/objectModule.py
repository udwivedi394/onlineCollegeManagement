class FacultySubject:
    def __init__(self,fac_subject_id, branch_sub_id, subject_code, 
                subject_name, branch_code, semester):
        self.faculty_subject_id = fac_subject_id
        self.branch_subject_id = branch_sub_id
        self.subject_code = subject_code
        self.subject_name = subject_name
        self.branch_code = branch_code
        self.semester = semester
